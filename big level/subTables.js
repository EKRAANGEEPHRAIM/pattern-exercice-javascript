//Generate the sub-tables

function subArrays(arr){

    const result = [];

    for(let i = 0 ; i < arr.length ; i++){
        let sub = [];
        for(let j = i ; j < arr.length; j++){
            sub.push(arr[j]);
            result.push([...sub])
        }
    }


    return result;
}



console.log(subArrays([1,2,3,4,7,5,9,10]))


